package Opps;

public class Suma {
	public void Sumar(int a, int b)
	{
		int c = a+b;
		System.out.println("La suma entre " + a + " + " + b + " es: " + c);
	}	
	
	public void Sumar(int a, int b, int c)
	{
		int d= a+b+c;
		System.out.println("La suma entre " + a + " + " + b + " + " + c + " es: " + d);
	}
	
	public double Sumar(double a, double b, double c)
	{
		double d= a+b;
		return d;
	}	
}
