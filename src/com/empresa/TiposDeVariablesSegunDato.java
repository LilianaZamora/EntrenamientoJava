package com.empresa;

public class TiposDeVariablesSegunDato {
	
	public static void main(String args[]) {
		
		byte edad = 20; //8 bits
		short numero = 32000; //16 bits
		int contador = 1000; //32 bits
		long distancia = 100000L; //64 bits
		float precio = 9.99f; //32 bits
		double salario = 12345.67; //64 bits
		char letra = 'A'; //16 bits
		boolean estado = true; //1 bit
		String nombre = "Liliana Zamora";	
		
	System.out.println("byte edad = " + edad);
	System.out.println("short numero = " + numero);
	System.out.println("int contador = " + contador);
	System.out.println("long distancia = " + distancia);
	System.out.println("float precio = " + precio);
	System.out.println("double salario = " + salario);
	System.out.println("char letra = " + letra);
	System.out.println("boolean estado = " + estado);
	System.out.println("String nombre = " + nombre);	
	}
}
